public class TerceiroExercicio {
    public static void main(String args[]){

        int num1 = 15;
        int num2 = 7;

        int soma = num1 + num2;
        int subtracao = num1 - num2;
        int multiplicacao = num1 * num2;
        float divisão = (float) num1 / num2;

        System.out.println("A soma das variáveis é: " + soma);
        System.out.println("A subtração das variáveis é: " + subtracao);
        System.out.println("A multiplicação das variáveis é: " + multiplicacao);
        System.out.println("A divisão das variáveis é: " + divisão);

    }
}
